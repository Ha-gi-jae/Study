2)자료 삭제 명령(DELETE)
 - 존재하는 자료를 삭제
 
 (사용형식)
 DELETE 테이블명
    [WHERE 조건];
      '테이블명' : 삭제할 자료가 저장된 테이블 
      'WHERE 조건' : 삭제할 조건을 기술하며 생략하면 모든 자료 삭제
      ROLLBACK의 대상
      관계가 설정된 자료는 삭제할 수 없다.
-- 관계연산자 크다 반대 작거나 같다, 작다 반대 크거나 같다, 같다 반대 같지 않다.      
       
