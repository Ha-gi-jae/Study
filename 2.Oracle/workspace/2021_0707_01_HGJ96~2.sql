1)자료 삽입 명령(INSERT)
 - 생성된 테이블에 새로운 자료를 삽입함
 (사용형식)
 INSERT INTO 테이블명[(컬럼명1[,컬럼명,...])]
  VALUES(값1[,값2,...]);
    '테이블명' : 자료 삽입 대상 테이블명
    '컬럼명1,..' : 이 옵션을 생략하면 테이블에 기술된 모든컬럼에 기술된 순서에 맞도록 VALUES절에 값을 기술해야함.
    '컬럼명1,..' : 이 옵션이 사용되면 필요한 컬럼에만 값(데이터)을 지정하여 자료를 삽입할 수 있음.
    기술된 '컬럼명'의 갯수 및 순서와 VALUES절의 값의 갯수 및 순서가 일치해야하고, NOT NULL항목은 반드시 기술해야함.(생략될 수 없음)
    
-- INSERT INTO 이렇게 외워야함. INSERT 만 ㄴㄴ.
--컬럼명이 생략되는 경우가 있음.
--생략되어지는 경우: 컬럼이 1개일 때, values절에서 값을 다 기입하는 경우[N.N]
--컬럼명과 컬럼순서, 값의 ㄱ 순서도 같아야함.
--사용자가 입력안하면 null이 들어감. null값이 사칙연산이 이루어지면 값이 null이 된다.

