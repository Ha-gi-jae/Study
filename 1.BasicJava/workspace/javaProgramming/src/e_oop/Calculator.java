package e_oop;

public class Calculator {
	// + - * / %
	//두개의 파라미터를 받고 계산해서 리턴해주면 된다.
	
	
	
	double plus(double a, double b){
		
		return a+b;
	}
	
	
	
	double minus(double a, double b){
		return a - b;
	}
	
	
	
	double gob(double a, double b){
		return a * b;
	}
	
	
	
	double div(double a, double b){
		return a / b;
	}
	
	
	
	double rem(double a, double b){
		return a % b;
	}
	
	
	
	
	
	
}
